package banks;

public class dictionaryDeposit {
    public dictionaryDeposit(double key, double value)
    {
        this.key = key;
        this.value = value;
    }
    private double key;
    private double value;
    public double getKey()
    {
        return key;
    }
    public void setKey(double key)
    {
        this.key = key;
    }
    public double getValue()
    {
        return value;
    }
    public void setValue()
    {
        this.value = value;
    }
}
